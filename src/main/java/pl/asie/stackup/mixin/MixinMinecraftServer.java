package pl.asie.stackup.mixin;

import net.minecraft.item.Item;
import net.minecraft.resources.IReloadableResourceManager;
import net.minecraft.server.MinecraftServer;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Shadow;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;
import pl.asie.stackup.StackUp;
import pl.asie.stackup.StackUpLoader;

@Mixin(MinecraftServer.class)
public abstract class MixinMinecraftServer {
	@Shadow private IReloadableResourceManager resourceManager;

	@Inject(method = "<init>", at = @At("RETURN"))
	private void onInit(CallbackInfo ci) {
		resourceManager.addReloadListener(new StackUpLoader());
	}
}
